s = "hfd-$@345ytASDFG5edsd7"

while len(s)>0:
    bukva = s[0]
    if bukva>='a' and bukva <='z':
        print(bukva, "small")
    elif bukva>='A' and bukva<='Z':
        print(bukva, "big")
    elif bukva.isdigit():
        print(bukva, "digat")
    else:
        print(bukva, "znak")
