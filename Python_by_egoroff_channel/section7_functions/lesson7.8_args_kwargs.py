# 7.8 *args и **kwargs Python. Передача аргументов в функцию

# Оператор *

a,b, c = 1,2,3
print(a,b,c)

a,b, c  = [True, 7, 'hello']
print(a,b,c)

# a,b,c = [True, 7, 'hello', 9, '54', 67, 4]
# print(a,b,c) # вызовет ошибку что много элементов для распаковки

a,*b,c = [True, 7, 'hello', 9, '54', 67, 4]
print(a,b,c, sep='\n')

*a,b,c = 'hello world'
print(a,b,c)

*a,b,c = [2,3]
print(a,b,c)

