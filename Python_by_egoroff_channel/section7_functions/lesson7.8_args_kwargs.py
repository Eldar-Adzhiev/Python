#=================================================================

def f(*args):
    print(args, type(args))

f(1,2,3,4,5,6,7,8)
f(1,3)
f()


#=================================================================

def f(**kwargs):
    print(kwargs, type(kwargs))

f(a=1, b=2,c=3)


#=================================================================

def f(*args, **kwargs):
    print(args, kwargs)

f(4,3,5,6,5,a=3,c=4)


#=================================================================

def out_print(*args, sep='#', end='@'):
    print(args, sep, end)

out_print(1,2,3,4,4,5, sep=90)


