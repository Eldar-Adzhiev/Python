# Теперь из html страницы выдел все ключи и значения


match = re.findall(r"([-\w]+)[ \t]*=[ \t]*[\"']([^\"']+)(?<![ \t])", text, re.MULTILINE)
print(match)

"""
-\w -  ключ может состоять из "-" и какихто символов
[ \t]* - перед = могут символы пробела или табуляции
[\"']([^\"']+) - в кавычках выделяем то что будет значением нашего ключа т.е. все символы кроме двойной и одинарной 
кавычки
(?<![ \t] - ретроспективная проверка которая смотрит если в конце появляется пробел или символ табуляции
мы останавливаемся и то что получили идет в значение ключа
"""
match = re.findall(r"""([-\w]+)         #вылеляем атоибут
                   [ \t]*=[ \t]*        # далее, должно идти равно и кавычки
                   (?P<q>[\"'])?        # поовеосем наличие кавычки
                   (?(q) ([^\"']+(?<![ \t]))|([^ \t>]+))         #выделяем значение атоибута
                   """,
                   text, re.MULTILINE | re.VERBOSE)
print(match)

