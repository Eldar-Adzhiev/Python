text = """<DOCTYPE html
<html>
<head>
<meta http-equiv="Content-Type" content="text/html"; charset=windows-1251>
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>Уроки по Python</title>
</head>
<body>
<script type="text/javascript">
let o = document.getElementById('id_div');
console.log(obj);
</script>
</body>
</html>
"""
match = re.findall(r"^<script.*?>([\w\W]+)(?=</script>)", text, re.MULTILINE)
print(match)
"""
^ - соответствует началу каждой строки т.е. в начале строки должен быть прописан '<script'
.*? -  внутри скрипта могут быть прописаны атрибуты. ? значит что квантификатор .* мы воспринимаем как минорный
т.е. будет искать последовательность минимальной длины
([\w\W]+) - выделяем все что будет стоять после тега <script> до тех пор пока мы не 
встретим это </script> закрывающий тег

"""

# Ретроспективная проверка

match = re.findall(r"^<script.*?>([\w\W]+)(?<=</script>)", text, re.MULTILINE)
print(match)

